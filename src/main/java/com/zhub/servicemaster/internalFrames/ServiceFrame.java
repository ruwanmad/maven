    private void formInternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameOpened
        Session session = HibernateUtil.getSessionFactory().openSession();

        this.loadVehicles(session);
        this.loadServiceBays(session);

        if (sale == null) {
            cmbVehicle.requestFocus();
        } else {
            cmbVehicle.setSelectedItem(sale.getVehicle().getVehicleNumber());
            txtLastMilage.setText(sale.getPreviousMilage() == null ? "" : "" + sale.getPreviousMilage());
            txtCurrentMilage.setText(sale.getCurrentMilage() == null ? "" : "" + sale.getCurrentMilage());
            txtNextMilage.setText(sale.getNextMilage() == null ? "" : "" + sale.getNextMilage());

            lblCustomerName.setText(sale.getVehicle().getBusinessPartner().getFirstName() + " " + sale.getVehicle().getBusinessPartner().getLastName());
            lblBpCode.setText(sale.getVehicle().getBusinessPartner().getBusinessPartnerCode());
            Set businessAddresses = sale.getVehicle().getBusinessPartner().getBusinessAddresses();
            for (Object object : businessAddresses) {
                if (object instanceof BusinessAddress) {
                    BusinessAddress businessAddress = (BusinessAddress) object;
                    Address address = businessAddress.getAddress();
                    lblAddress1.setText(address.getAdressLine1());
                    lblAddress2.setText(address.getAdressLine2());
                    lblAddress3.setText(address.getAdressLine3());
                }
            }

            cmbServiceBay.setSelectedItem(sale.getServiceBay().getServiceBayName());
            if (sale.getSaleStatus().getStatusId() != 1
                    && sale.getSaleStatus().getStatusId() != 2
                    && sale.getSaleStatus().getStatusId() != 6) {
                btnSettle.setEnabled(true);
                btnInvoice.setEnabled(true);
            } else if (sale.getSaleStatus().getStatusId() != 1) {
                btnInvoice.setEnabled(true);
            }
            dateServiceDate.setDate(sale.getCreatedDate());

            DefaultTableModel tableModel = (DefaultTableModel) tblItems.getModel();
            tableModel.setRowCount(0);
            Set<SaleItem> saleItems = sale.getSaleItems();

            for (SaleItem saleItem : saleItems) {
                if (saleItem.getSaleItemStatus().getItemStatusId() == 1) {
                    Item item = saleItem.getItem();

                    saleItemMap.put(item.getItemCode(), saleItem);

                    String itemCode = item.getItemCode();
                    String itemName = item.getItemName();
                    float quantity = saleItem.getQuantity();
                    float unitPrice = ItemFunctions.getItemSellingPrice(item);
                    float subTotal = saleItem.getSubTotal();
                    float discount = saleItem.getDiscount();
                    float total = saleItem.getTotal();

                    tableModel.addRow(new Object[]{itemCode, itemName, quantity, unitPrice, subTotal, discount, total});

                    grandSubTotal += subTotal;
                    grandDiscount += discount;
                    grandTotal += total;
                }
            }

            Set invoices = sale.getInvoices();
            for (Object object : invoices) {
                if (object instanceof Invoice) {
                    this.invoice = (Invoice) object;
                }
            }

            txtGrandSubTotal.setText("" + grandSubTotal);
            txtGrandDiscount.setText("" + grandDiscount);
            txtGrandTotal.setText("" + grandTotal);

            this.btnSave.setText("Update");

            SwingUtilities.invokeLater(new Runnable() {
                @Override
                public void run() {
                    txtItemSearchCode.requestFocus();
                }
            });

            sale = (Sale) session.load(Sale.class, sale.getSaleCode());

            getRootPane().setBorder(BorderFactory.createLineBorder(SystemData.BORDER_COLOR, 2));
        }

        session.close();

        SearchAction searchAction = new SearchAction(this);
        String searchKey = "Search (F2)";
        btnItemSearch.setAction(searchAction);
        btnItemSearch.setText(searchKey);
        btnItemSearch.getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW).put(KeyStroke.getKeyStroke(KeyEvent.VK_F2, 0), searchKey);
        btnItemSearch.getActionMap().put(searchKey, searchAction);
    }//GEN-LAST:event_formInternalFrameOpened

    private void cmbVehicleItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbVehicleItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            String vehicleNo = (String) cmbVehicle.getSelectedItem();
            if (!vehicleNo.isEmpty()) {
                Session session = HibernateUtil.getSessionFactory().openSession();
                session.beginTransaction();

                Vehicle vehicle = (Vehicle) session
                        .createCriteria(Vehicle.class)
                        .add(Restrictions.eq("vehicleNumber", vehicleNo.trim()))
                        .uniqueResult();
                BusinessPartner businessPartner = vehicle.getBusinessPartner();

                String firstName = businessPartner.getFirstName();
                if (!firstName.equalsIgnoreCase("CASH")) {
                    lblCustomerName.setText(businessPartner.getFirstName() + " " + businessPartner.getLastName());

                    Set addresses = businessPartner.getBusinessAddresses();
                    if (!addresses.isEmpty()) {
                        for (Object object : addresses) {
                            if (object instanceof BusinessAddress) {
                                BusinessAddress businessAddress = (BusinessAddress) object;
                                Address address = businessAddress.getAddress();
                                this.lblAddress1.setText(address.getAdressLine1());
                                this.lblAddress2.setText(address.getAdressLine2());
                                this.lblAddress3.setText(address.getAdressLine3());
                            }
                        }
                    }
                } else {
                    lblCustomerName.setText(businessPartner.getFirstName());
                    this.lblAddress1.setText("");
                    this.lblAddress2.setText("");
                    this.lblAddress3.setText("");
                }

                lblBpCode.setText(businessPartner.getBusinessPartnerCode().trim());

                List<Sale> sales = session
                        .createCriteria(Sale.class)
                        .add(Restrictions.eq("vehicle", vehicle))
                        .addOrder(Order.desc("createdDate"))
                        .addOrder(Order.desc("createdTime"))
                        .list();

                if (sales.isEmpty()) {
                    txtLastMilage.setText("");
                } else {
                    for (Sale tempSale : sales) {
                        if (tempSale.getPreviousMilage() != null) {
                            float lastMilage = tempSale.getPreviousMilage();
                            txtLastMilage.setText("" + (int) Math.round(lastMilage));
                        } else {
                            txtLastMilage.setText("");
                        }

                        if (tempSale.getNextMilage() != null) {
                            float nextMilage = tempSale.getNextMilage();
                            txtCurrentMilage.setText("" + (int) Math.round(nextMilage));
                        }
                        break;
                    }
                }
            }
        }
    }//GEN-LAST:event_cmbVehicleItemStateChanged

